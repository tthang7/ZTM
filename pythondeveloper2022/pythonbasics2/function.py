# default parameters
def say_hello(name='dark', emoji='😁'):
    print(f'hellloooo {name} {emoji}')

# positional arguments
say_hello('justin', '😁')
say_hello('jie', '😁')

# keyword arguments
say_hello('white')
say_hello()
